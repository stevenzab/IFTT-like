@app.route('/createAcrea', methods=['POST'])
def createAcrea():
    token = request.headers.get('Authorization')
    id_service_action = request.json.get('id_service_action')
    id_service_reaction = request.json.get('id_service_reaction')
    id_action = request.json.get('id_action')
    id_reaction = request.json.get('id_reaction')
    arguments = request.json.get('params_action')
    params = request.json.get('params_reaction')

    return (createActionReaction(conn, token, id_service_action, id_service_reaction, id_action, id_reaction, arguments, params))

